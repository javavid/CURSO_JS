/* ciclo while
- es similar a for, pero con la diferencia de que solo tenemos un condicional.
- Mientras se cumpla el condicional se ejecutara el siclo. 
*/
let contador = 0;
while(contador <= 10){
console.log(contador);
contador++;
}


/* ciclo Do While
- es similar a ciclo While, con la diferencia de que ejecuta el bloque de codigo al menos una vez.
*/
let contar = 100;
do{
    console.log(contar);
    contar++;
}while(contar <= 10);